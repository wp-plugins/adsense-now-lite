<h2>Now! Plugin for AdSense<br>
  <small>Get Started with AdSense Now!</small>
</h2>
<p><em>AdSense Now!</em> is the simplest possible way to generate revenue from your blog using Google AdSense. Aiming at simplicity, this plugin does only one thing: it puts your AdSense code in up to three spots in your posts and pages (both existing ones and those yet to be written).</p>

<p><strong>AdSense Now! (AKA Now! Plugin for AdSense)</strong>: This plugin is for those who find the features of other AdSense plugins too overwhelming. Using AdSense Now!, you can quickly add up to three ad blocks (with the same ad code) on your posts and pages.</p>

